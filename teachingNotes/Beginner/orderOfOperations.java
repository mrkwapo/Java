package com.Dracodess;

public class Main {

    public static void main(String[] args) {
        //Order of Operations: parenthesis(), division /, multiplication *, addition +, then subtraction -       
        int x = 10 + 3 * 2;
        System.out.println(x); //16
        
        int y = (10 + 3) * 2;
        System.out.println(y); //26

        int z = 10 / 5 * 2;
        System.out.println(z);//4

        int w = 10 + 5 - 2;
        System.out.println(w); //13
    }
}
